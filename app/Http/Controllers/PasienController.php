     */
    public function store(Request $request)
    {
        //
        $request->validate([
            'nama' => 'required',
            'no_ktp' => 'required',
            'jenis_kelamin' => 'required',
            'ttl' => 'required',
            'usia' => 'required',
            'alamat' => 'required'
        ]);

        Pasien::create($request->all());
        return redirect('/pasien')->with('status', 'Data Dokter Berhasil Ditambahkan!');
    }

